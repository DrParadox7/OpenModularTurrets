package modularTurrets.models.renderers.itemRenderers;

import modularTurrets.blocks.BlockIDs;
import net.minecraftforge.client.MinecraftForgeClient;

public class ItemRenderers {
	
	public static void init()
	{
		MinecraftForgeClient.registerItemRenderer(BlockIDs.disposableItemTurret, new DisposableItemTurretItemRenderer());
		MinecraftForgeClient.registerItemRenderer(BlockIDs.rocketTurret, new RocketTurretItemRenderer());
		MinecraftForgeClient.registerItemRenderer(BlockIDs.machineGunTurret, new MachineGunTurretItemRenderer());
		MinecraftForgeClient.registerItemRenderer(BlockIDs.grenadeTurret, new GrenadeLauncherTurretItemRenderer());
		MinecraftForgeClient.registerItemRenderer(BlockIDs.laserTurret, new LaserTurretItemRenderer());
		MinecraftForgeClient.registerItemRenderer(BlockIDs.lever, new LeverItemRenderer());
	}
}
